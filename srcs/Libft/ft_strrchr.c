char		*ft_strrchr(const char *src, int c)
{
	char	*last_ocr;

	last_ocr = 0;
	while (1)
	{
		if (*src == c)
			last_ocr = (char *)src;
		if (!(*src))
			break ;
		src++;
	}
	return (last_ocr);
}
